#include "fvCFD.H"
#include "dynamicFvMesh.H"
#include "simpleControl.H"
#include "fvOptions.H"

// * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * //

int main(int argc, char *argv[])
{
    #include "setRootCaseLists.H"
    #include "createTime.H"
    #include "createDynamicFvMesh.H"
    #include "createFields.H"
    #include "createTimeControls.H"

    simpleControl simple(mesh);

    // * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * //

    Info<< "\nStarting level set etching simulation\n" << endl;

    while (simple.loop())  // Removed runTime argument
    {
        Info<< "Time = " << runTime.timeName() << nl << endl;

        // Calculate the etch rate based on angular flux
        #include "calculateEtchRate.H"

        // Solve the level set equation
        #include "solveLS.H"


        // Update the mesh based on the level set function (optional)
        // #include "updateMesh.H"

        runTime.write();

        Info<< "ExecutionTime = " << runTime.elapsedCpuTime() << " s"
            << "  ClockTime = " << runTime.elapsedClockTime() << " s"
            << nl << endl;
    }

    Info<< "End\n" << endl;

    return 0;
}

